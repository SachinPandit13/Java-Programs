

public class integerDemo {
    public static void main(String[] args) {
        Integer val1 = 21122;
        Integer val2 = 43222;

        int intValue = val1.intValue();
        long longValue = val2.longValue();

        System.out.println("The int value of the Integer=" + intValue);
        System.out.println("The long value of the Integer=" + longValue);
    }
}